void convert(int n)
{
	if (n > 0) {
		if (n > 9) {
			printf("%d ", n % 10);
			convert(n / 10);
		}
		if (n < 9)
			printf("%d ", n % 10);
	}
	if (n < 0) {
		if (-n == 2147483648) {
			n = n / 10;
			n = -n;
			printf("%d ", 8);
			convert(n);
			printf("- ");
		}
		else {
			n = -n;
			convert(n);
			printf("- ");

		}
	}
}

